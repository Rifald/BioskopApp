    private void pesanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanBtnActionPerformed
        // TODO add your handling code here:
        
         if("".equals(naPesTf.getText())){
                JOptionPane.showMessageDialog(null, "Harap Masukkan Nama Pemesan", "Status", JOptionPane.ERROR_MESSAGE, null);
            }else if("--Pilih Bioskop--".equals(bioskopComboBox.getSelectedItem().toString())){
                JOptionPane.showMessageDialog(null, "Harap Memilih Bioskop", "Status", JOptionPane.ERROR_MESSAGE, null);
            }else if("--Pilih Film--".equals(filmComboBox.getSelectedItem().toString()) || "".equals(filmComboBox.getSelectedItem().toString()) ){
                JOptionPane.showMessageDialog(null, "Harap Memilih Film", "Status", JOptionPane.ERROR_MESSAGE, null);
            }else if("".equals(bayarTf.getText())){
                JOptionPane.showMessageDialog(null, "Harap Membayar", "Status", JOptionPane.ERROR_MESSAGE, null);
            }else if(bayarTf.getText().matches("[0-9]+") == false){
                JOptionPane.showMessageDialog(null, "Harap Memasukkan Nominal Pembayaran", "Status", JOptionPane.ERROR_MESSAGE, null);
            }else if(Integer.parseInt(bayarTf.getText()) > Integer.parseInt(hrgL.getText()) ||Integer.parseInt(bayarTf.getText()) < Integer.parseInt(hrgL.getText()) ){
                JOptionPane.showMessageDialog(null, "Harap Membayar Sesuai Harga", "Status", JOptionPane.ERROR_MESSAGE, null);
            }else if(baris==null||nomer==null){
                JOptionPane.showMessageDialog(null, "Harap Memilih Bangku Tempat Duduk", "Status", JOptionPane.ERROR_MESSAGE, null);
            }else{
             try {
           
            Pesan CP  = new Pesan(username, bioskopComboBox.getSelectedItem().toString(), filmComboBox.getSelectedItem().toString(), studioL.getText(), tglL.getText(), jamL.getText(), naPesTf.getText(), baris, nomer, hrgL.getText());
            if (CP.masukkanData()) {
                JOptionPane.showMessageDialog(null, ""+bioskopComboBox.getSelectedItem().toString()+""
                        + "\n"
                        + "\n--------------------------------------"
                        + "\n"
                        + "\nJudul : " + filmComboBox.getSelectedItem().toString()
                        + "\nTanggal : " + tglL.getText()
                        + "\nWaktu : " + jamL.getText()
                        + "\nBaris : " + baris + " Nomor : " + nomer + ""
                        + "\nHarga : " + hrgL.getText()
                        + "\nStudio : " + studioL.getText()
                        + "\nNama Pemesan : " + naPesTf.getText()
                        + "", "Status", JOptionPane.INFORMATION_MESSAGE, null);
                System.out.println("Berhasil");
                
                Bangku ba = new Bangku();
                String bangkuNya = baris + nomer;
                ba.setBangku(studioL.getText(), bioskopComboBox.getSelectedItem().toString(), bangkuNya);
                

                ba.getDataBangku(studioL.getText(), bioskopComboBox.getSelectedItem().toString(), bangku);
                
                setBangku(bangku,0,bangkuA1);
                setBangku(bangku,1,bangkuA2);
                setBangku(bangku,2,bangkuA3);
                setBangku(bangku,3,bangkuA4);
                
                setBangku(bangku,4,bangkuB1);
                setBangku(bangku,5,bangkuB2);
                setBangku(bangku,6,bangkuB3);
                setBangku(bangku,7,bangkuB4);
                
                setBangku(bangku,8,bangkuC1);
                setBangku(bangku,9,bangkuC2);
                setBangku(bangku,10,bangkuC3);
                setBangku(bangku,11,bangkuC4);
            } else {
                JOptionPane.showMessageDialog(null, "Gagal", "Status", JOptionPane.ERROR_MESSAGE, null);
            }
        } catch (SQLException ex) {
            
        }
        }
        
    }//GEN-LAST:event_pesanBtnActionPerformed

