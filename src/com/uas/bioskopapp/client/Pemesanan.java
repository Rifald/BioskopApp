    private void bioskopComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_bioskopComboBoxItemStateChanged
        // TODO add your handling code here:
        if(bioskopDipilih){
            DefaultComboBoxModel spesFilm = new DefaultComboBoxModel();
            filmComboBox.setModel(spesFilm);
            try{
                Booking bk = new Booking();
                if(bioskopComboBox.getSelectedItem()=="--Pilih Bioskop--"){
                
                judulL.setText("-");
                genL.setText("-");
                durL.setText("-");
                tglL.setText("-");
                jamL.setText("-");
                hrgL.setText("-");
                }else{
                bk.insertFilmComboBox(spesFilm, bioskopComboBox.getSelectedItem().toString());
                bk.getStudio(studioL, bioskopComboBox.getSelectedItem().toString(), filmComboBox.getSelectedItem().toString());
                }
            }catch(Exception e){
                System.out.println(e.getMessage());
            }
            
            bioskopDipilih = false;
        }
    }//GEN-LAST:event_bioskopComboBoxItemStateChanged

